export const landingPageSlugs = areas.flatMap((area) => [
  `${area.slug}-deals`,
  `${area.slug}-ramadan`,
]);

export function parseLandingSlug(slug: string):
  | { area: Area; type: PageType }
  | null {
  const match = slug.match(/^(.*)-(deals|ramadan)$/);
  if (!match) return null;
  const areaSlug = match[1];
  const type = match[2] as PageType;
  const area = areas.find((item) => item.slug === areaSlug);
  if (!area) return null;
  return { area, type };
}

export function getLandingPageData(slug: string) {
  const parsed = parseLandingSlug(slug);
  if (!parsed) return null;
  const restaurants =
    parsed.type === "deals" ? parsed.area.deals : parsed.area.ramadan;
  return { ...parsed, restaurants };
}
